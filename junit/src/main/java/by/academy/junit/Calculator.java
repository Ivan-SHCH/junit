package by.academy.junit;

public class Calculator {

	public static int getSum(int i, int b) {
		return i + b;
	}

	public static int getDivide(int x, int y) {
		return x / y;
	}

	public static int getMultiple(int x, int y) {
		return x * y;
	}

	public static int square(int x) {
		return x * x;
	}
}
